def twenty_seventeen():
    """Come up with the most creative expression that evaluates to 2017,
    using only numbers and the +, *, and - operators.

    >>> twenty_seventeen()
    2017
    >>> twenty_seventeen() + twenty_seventeen()
    4034
    >>> twenty_seventeen() * 3
    6051
    """
    return (1 + 2) * 2 * 2 * 2 * 2 * 2 * 3 * (3 + 4) + 1
